package com.example.guannan.recyclerview_operation.bean;

/**
 * @author guannan
 * @date 2018/2/28 16:50
 */

public class Sticky {

    private String category;
    private String content;

    public Sticky(String category, String content) {
        this.category = category;
        this.content = content;
    }

    public String getCategory() {
        return category;
    }

    public String getContent() {
        return content;
    }
}
